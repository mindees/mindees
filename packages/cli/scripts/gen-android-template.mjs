/**
 * Codegen for the experimental `android` create template.
 *
 * Reads the CI-verified reference Android host (examples/native-hosts/android — the
 * SAME tree that builds + emulator-smoke-passes in native-android.yml) and emits
 * `packages/cli/src/android-template.generated.ts`: a path → contents map the CLI's
 * `android` template scaffolds. Generating from the real tree (instead of hand-copying
 * Kotlin into a string) means the vendored host can never silently drift from the
 * verified source — `check:android-template` regenerates and `git diff`s to enforce it.
 *
 * The file list comes from `git ls-files` (the 28 tracked text files only — never an
 * untracked `build/`, `.gradle/`, or a bootstrapped `gradle-wrapper.jar`), so no binary
 * or stale artifact can leak in. Most files are COPIED verbatim; the app-js build glue
 * is TRANSFORMED to resolve `@mindees/*` from npm (not the monorepo); `package.json`,
 * the README, and `.gitignore` are SYNTHESIZED; tests + the built bundle are OMITTED.
 *
 * Run: `pnpm --filter @mindees/cli run gen:android-template`
 *
 * @module
 */

import { execFileSync } from 'node:child_process'
import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(here, '..', '..', '..') // packages/cli/scripts → repo root
const EXAMPLE_DIR = 'examples/native-hosts/android'
const PREFIX = `${EXAMPLE_DIR}/`
const TEMPLATE_OVERRIDES = join(here, 'android-template')
const GENERATED = resolve(repoRoot, 'packages/cli/src/android-template.generated.ts')

if (!existsSync(resolve(repoRoot, EXAMPLE_DIR))) {
  throw new Error(`gen-android-template: ${EXAMPLE_DIR} not found (run from the monorepo).`)
}

/** The locked @mindees/* version line — pinned into the synthesized app-js package.json. */
const versionSrc = readFileSync(resolve(repoRoot, 'packages/cli/src/version.ts'), 'utf8')
const VERSION = /VERSION\s*=\s*'([^']+)'/.exec(versionSrc)?.[1]
if (!VERSION) throw new Error('gen-android-template: could not read VERSION from version.ts')

/** The exact tracked files of the reference host (deterministic order, zero binaries). */
const tracked = execFileSync('git', ['ls-files', EXAMPLE_DIR], { cwd: repoRoot, encoding: 'utf8' })
  .split('\n')
  .filter(Boolean)

const readExample = (relKey) => readFileSync(resolve(repoRoot, PREFIX + relKey), 'utf8')
const readOverride = (name) => readFileSync(join(TEMPLATE_OVERRIDES, name), 'utf8')

/** Files NOT shipped in the scaffold: host tests, the built bundle, the example READMEs. */
const isOmitted = (relKey) =>
  relKey.includes('/src/test/') ||
  relKey.includes('/src/androidTest/') ||
  relKey === 'mindees-example-app/src/main/assets/mindees-app.bundle.js' ||
  relKey === 'README.md' ||
  relKey === 'mindees-example-app/app-js/README.md' ||
  relKey === '.gitignore' // synthesized below (example body + the generated-bundle line)

/** App-js build glue rewritten to resolve @mindees/* from npm instead of the monorepo. */
const TRANSFORMED = {
  'mindees-example-app/app-js/tsdown.config.ts': () => readOverride('tsdown.config.ts.template'),
  'mindees-example-app/app-js/scripts/gen-routes.mjs': () =>
    readOverride('gen-routes.mjs.template'),
}

/**
 * In-place content patches that inject scaffold placeholders into otherwise-verbatim files.
 * `materialize` substitutes them per app: `{{androidAppId}}` (a unique install id so two scaffolds
 * coexist on a device) and `{{appName}}` (the Gradle project name). The compile `namespace` stays
 * `dev.mindees.example` — Android keys install identity on `applicationId`, not the code package.
 */
const CONTENT_PATCHES = {
  'mindees-example-app/build.gradle.kts': (c) =>
    c.replace('applicationId = "dev.mindees.example"', 'applicationId = "{{androidAppId}}"'),
  'settings.gradle.kts': (c) =>
    c.replace(
      'rootProject.name = "mindees-native-host-android"',
      'rootProject.name = "{{appName}}"',
    ),
}

const files = {}
for (const path of tracked) {
  if (!path.startsWith(PREFIX)) continue
  const relKey = path.slice(PREFIX.length)
  if (isOmitted(relKey)) continue
  let content = (TRANSFORMED[relKey] ?? (() => readExample(relKey)))()
  if (CONTENT_PATCHES[relKey]) content = CONTENT_PATCHES[relKey](content)
  files[relKey] = content
}

// SYNTHESIZED files (not present, or replaced, in the reference tree).
files['mindees-example-app/app-js/package.json'] = readOverride('package.json.template').replaceAll(
  '__VERSION__',
  VERSION,
)
files['README.md'] = readOverride('README.md.template')
files['.gitignore'] =
  `${readExample('.gitignore').trimEnd()}\n\n# The JS bundle is generated by the app-js build (never vendored).\nmindees-example-app/src/main/assets/mindees-app.bundle.js\n`

// Emit a deterministic module: sorted keys, JSON.stringify'd values (byte-exact
// round-trip through Kotlin/TSX backticks/${}/quotes), trailing newline. No biome pass
// (the file is in biome's ignore list) so regeneration is a guaranteed no-op diff.
const sortedKeys = Object.keys(files).sort()
let out = ''
out += '// @generated by scripts/gen-android-template.mjs — do not edit by hand.\n'
out += '// Regenerate: `pnpm --filter @mindees/cli run gen:android-template`\n'
out += '// Source of truth: examples/native-hosts/android/ (the CI-verified reference host).\n\n'
out +=
  '/** Files the experimental `android` template scaffolds (project-relative path → contents). */\n'
out += 'export const ANDROID_TEMPLATE_FILES: Record<string, string> = {\n'
for (const key of sortedKeys) {
  out += `  ${JSON.stringify(key)}: ${JSON.stringify(files[key])},\n`
}
out += '}\n\n'
out += '/** The @mindees/* version the scaffolded app-js pins to (the line that generated it). */\n'
out += `export const ANDROID_TEMPLATE_VERSION = ${JSON.stringify(VERSION)}\n`

writeFileSync(GENERATED, out)
process.stdout.write(
  `gen-android-template: wrote ${sortedKeys.length} files (pinned @mindees/* ${VERSION})\n`,
)
